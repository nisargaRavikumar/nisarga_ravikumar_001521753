package model;


public class CheckingAccInfo {
    private String Bname;
    private String BRountingNo;
    private String BAccNo;
    private String AccBalance;
    private String AccType;

    public String getBname() {
        return Bname;
    }

    public void setBname(String Bname) {
        this.Bname = Bname;
    }

    public String getBRountingNo() {
        return BRountingNo;
    }

    public void setBRountingNo(String BRountingNo) {
        this.BRountingNo = BRountingNo;
    }

    public String getBAccNo() {
        return BAccNo;
    }

    public void setBAccNo(String BAccNo) {
        this.BAccNo = BAccNo;
    }

    public String getAccBalance() {
        return AccBalance;
    }

    public void setAccBalance(String AccBalance) {
        this.AccBalance = AccBalance;
    }

    public String getAccType() {
        return AccType;
    }

    public void setAccType(String AccType) {
        this.AccType = AccType;
    }
}
