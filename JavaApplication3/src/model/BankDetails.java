public class BankDetails {
    private String CBname;
    private String CBRountingNo;
    private String CBAccNo;
    private String CAccBalance;

    public String getCBname() {
        return CBname;
    }

    public void setCBname(String CBname) {
        this.CBname = CBname;
    }

    public String getCBRountingNo() {
        return CBRountingNo;
    }

    public void setCBRountingNo(String CBRountingNo) {
        this.CBRountingNo = CBRountingNo;
    }

    public String getCBAccNo() {
        return CBAccNo;
    }

    public void setCBAccNo(String CBAccNo) {
        this.CBAccNo = CBAccNo;
    }

    public String getCAccBalance() {
        return CAccBalance;
    }

    public void setCAccBalance(String CAccBalance) {
        this.CAccBalance = CAccBalance;
    }

    
}
